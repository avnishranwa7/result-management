    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        try{
            Connection conn = createConnection();
            
            if(jTextField1.getText().equals("") || jTextField2.getText().equals("") || jTextField3.getText().equals("") || jTextField4.getText().equals("") ||
                    jTextField5.getText().equals("") || jTextField6.getText().equals("")){
                JOptionPane.showMessageDialog(rootPane, "All fields are required");
            }
            else{
                
                name = jTextField1.getText();
                address = jTextField2.getText();
                city = jTextField3.getText();
                country = jTextField4.getText();
                pinCode = Integer.parseInt(jTextField5.getText());
                score = Integer.parseInt(jTextField6.getText());

                if(score>30){
                    pass = true;
                }
                
                PreparedStatement pstmt = conn.prepareStatement("insert into sat_score values(?,?,?,?,?,?,?)");
                
                pstmt.setString(1, name);
                pstmt.setString(2, address);
                pstmt.setString(3, city);
                pstmt.setString(4, country);
                pstmt.setInt(5, pinCode);
                pstmt.setInt(6, score);
                pstmt.setBoolean(7, pass);
                pstmt.executeUpdate();

                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField5.setText("");
                jTextField6.setText("");
            }
        }
        catch(SQLException e){
              if(e.getMessage().equals("Duplicate entry '" + name + "' for key 'sat_score.PRIMARY'")){
                  JOptionPane.showMessageDialog(null, "This name already exists");
              }
              else{
                    System.out.println(e.getMessage());
              }
        }catch(NumberFormatException e){
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "Pincode and Score fields can only contain numbers");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

