    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            Connection connectDB = createConnection();
            Statement stmt = connectDB.createStatement();
            ResultSet rs = stmt.executeQuery("Select * from sat_score");
            
            String result = "";
            while(rs.next()){
                JSONObject currRecord = new JSONObject();
                currRecord.put("Name", rs.getString(1));
                currRecord.put("Address", rs.getString(2));
                currRecord.put("City", rs.getString(3));
                currRecord.put("Country", rs.getString(4));
                currRecord.put("Pincode", rs.getString(5));
                currRecord.put("Score", rs.getString(6));
                
                String passFail = "Fail";
                if(rs.getString(7).equals("1")){
                    passFail = "Pass";
                }
                currRecord.put("Pass/Fail", passFail);
                
                result+=(currRecord.toJSONString() + "\n");
            }
            jTextArea1.setText(result);
        }
        catch(SQLException e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

